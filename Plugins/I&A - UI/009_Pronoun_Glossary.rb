#===============================================================================
# Pronoun Message Command Glossary
#===============================================================================
#
# PLAYER
#   \psub   = player subject pronoun        # he, she, they
#   \PSub   = player subject, proper case   # He, She, They
#   \PSUB   = player subject, full caps     # HE, SHE, THEY
#
#   \pobj   = player object pronoun         # him, her, them
#   \PObj   = player object, proper case    # Him, Her, Them
#   \POBJ   = player object, full caps      # HIM, HER, THEM
#
#   \ppos   = player possessive pronoun     # his, her, their
#   \PPos   = player possessive, proper     # His, Her, Their
#   \PPOS   = player possessive, full caps  # HIS, HER, THEIR
#
#   \pcon   = player contraction            # he's, she's, they're
#   \PCon   = player contraction, proper    # He's, She's, They're
#   \PCON   = player contraction, full caps # HE'S, SHE'S, THEY'RE
#
#   \pverb  = player verb                   # is, are
#   \PVerb  = player verb, proper           # Is, Are
#   \PVERB  = player verb, full caps        # IS, ARE
#
#   \pc     = player name colour code       # b/r/g
#
#
# RIVAL 1
#   \r1name = Rival 1 name
#   \r1c    = Rival 1 name colour code
#
#   \r1sub  = Rival 1 subject pronoun
#   \R1Sub  = Rival 1 subject, proper case
#   \R1SUB  = Rival 1 subject, full caps
#
#   \r1obj  = Rival 1 object pronoun
#   \R1Obj  = Rival 1 object, proper case
#   \R1OBJ  = Rival 1 object, full caps
#
#   \r1pos  = Rival 1 possessive pronoun
#   \R1Pos  = Rival 1 possessive, proper
#   \R1POS  = Rival 1 possessive, full caps
#
#   \r1con  = Rival 1 contraction
#   \R1Con  = Rival 1 contraction, proper
#   \R1CON  = Rival 1 contraction, full caps
#
#   \r1verb = Rival 1 verb
#   \R1Verb = Rival 1 verb, proper
#   \R1VERB = Rival 1 verb, full caps
#
#
# RIVAL 2
#   \r2name = Rival 2 name
#   \r2c    = Rival 2 name colour code
#
#   \r2sub  = Rival 2 subject pronoun
#   \R2Sub  = Rival 2 subject, proper case
#   \R2SUB  = Rival 2 subject, full caps
#
#   \r2obj  = Rival 2 object pronoun
#   \R2Obj  = Rival 2 object, proper case
#   \R2OBJ  = Rival 2 object, full caps
#
#   \r2pos  = Rival 2 possessive pronoun
#   \R2Pos  = Rival 2 possessive, proper
#   \R2POS  = Rival 2 possessive, full caps
#
#   \r2con  = Rival 2 contraction
#   \R2Con  = Rival 2 contraction, proper
#   \R2CON  = Rival 2 contraction, full caps
#
#   \r2verb = Rival 2 verb
#   \R2Verb = Rival 2 verb, proper
#   \R2VERB = Rival 2 verb, full caps
#
#
# NAME BOX EXAMPLES
#
